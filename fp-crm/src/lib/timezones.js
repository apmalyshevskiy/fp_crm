// разница пояса с Москвой (МСК = UTC+3)
const MSK_DIFF = {
  'Europe/Kaliningrad': -1, 'Europe/Moscow': 0, 'Europe/Samara': 1, 'Asia/Yekaterinburg': 2,
  'Asia/Omsk': 3, 'Asia/Krasnoyarsk': 4, 'Asia/Irkutsk': 5, 'Asia/Yakutsk': 6,
  'Asia/Vladivostok': 7, 'Asia/Magadan': 8, 'Asia/Kamchatka': 9,
}
export function mskLabel(tz) {
  const d = MSK_DIFF[tz]
  if (d == null) return ''
  return d === 0 ? 'МСК' : (d > 0 ? `МСК +${d}` : `МСК −${Math.abs(d)}`)
}

